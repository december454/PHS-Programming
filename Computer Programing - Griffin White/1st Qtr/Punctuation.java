//GRADE = 100
public class Punctuation {

    public static void main (String [] args) 
    {
    	System.out.println("\"Your neighbor\'s wife looks prettier than your own.\" - Chinese Proverb");/*Again, I am using the "\'" instead of just "'".*/
    	System.out.println();
    	System.out.println("Bei\'jing\"\\Olympics\"\\\\2008");
    	System.out.println();
    	System.out.println("Mustached man with a toupee, goatee, top hat and bow tie: =[:{(>X");
    	System.out.println();
    	System.out.println("Here are \"two\" tabs.\t\tWasn\'t that easy?");
    	System.out.println();
    	System.out.println("I put \"double quotes\" around quotations.");
    	System.out.println();
    	System.out.println("I put \'single quotes\' in words like I\'d, she\'ll, and won\'t.");
    	System.out.println();
    	System.out.println("When typing an escape sequence, you need to use the \\ key.");
    	System.out.println();/*Again, I thought adding the extra clear line was nice.*/
  
    	
    }
    
    
}